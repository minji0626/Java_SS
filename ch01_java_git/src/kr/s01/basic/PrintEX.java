package kr.s01.basic;

public class PrintEX {

	public static void main(String[] args) {
		//문자열
		//출력 후 줄바꿈
		System.out.println("Hello World!!");
		
		//출력 후 줄바꿈 없음
		//System.out.print("봄");
		
		//문자
		System.out.println('A');
		
		//숫자
		System.out.println(23);
		
		//논리값
		System.out.println(true);
		
		//포맷문자 전달될 데이터
		System.out.printf("%c\n",'A');
	}

}
